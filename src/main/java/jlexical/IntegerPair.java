public class IntegerPair {
    private int former;
    private int latter;

    public IntegerPair(int former, int latter) {
        this.former = former;
        this.latter = latter;
    }

    public int getFormer() {
        return former;
    }

    public int getLatter() {
        return latter;
    }

}
